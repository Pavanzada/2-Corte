    private void recargarLista() {
        jList1.setModel(new javax.swing.AbstractListModel() {
            Object[] strings = respuestas.toArray();

            public int getSize() {
                return strings.length;
            }

            public Object getElementAt(int i) {
                return strings[i];
            }
        });
    }

    private void limpiar() {
        jtDescripcion.setText("");
        jtId.setText("");
        jtNombre.setText("");
        jtPorcentaje.setValue(null);
        respuestas = new Stack<>();
        recargarLista();
    }

    public void cargarPregunta(Pregunta p) {
        editar = p;
        jcComplejidad.setSelectedItem(editar.getComplejidad());
        jtDescripcion.setText(editar.getDescripcion());
        jtNombre.setText(editar.getNombre());
        jtPorcentaje.setValue(editar.getPorcentaje());
        jtId.setText(editar.getId() + "");
        respuestas = editar.getRespuestas();
        recargarLista();
        for (Respuesta r : respuestas){
            if (r.getValor().equals(editar.getRespuesta())){
                jList1.setSelectedValue(r, true);
                break;
            }
        }
    }

