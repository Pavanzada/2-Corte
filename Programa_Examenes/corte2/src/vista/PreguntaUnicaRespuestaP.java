    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (editar == null) {
            editar = c.getBuilder().getUnicaRespuesta();
            editar.setComplejidad(jcComplejidad.getSelectedItem().toString());
            editar.setDescripcion(jtDescripcion.getText());
            editar.setNombre(jtNombre.getText());
            Long l = (Long) jtPorcentaje.getValue();
            editar.setPorcentaje(l.floatValue());
            editar.setRespuestas(respuestas);
            editar.setRespuesta(jList1.getSelectedValue().toString());
            c.agregarPregunta(editar);
        } else {
            editar.setComplejidad(jcComplejidad.getSelectedItem().toString());
            editar.setDescripcion(jtDescripcion.getText());
            editar.setNombre(jtNombre.getText());
            if (jtPorcentaje.getValue() instanceof Long){
                Long l = (Long) jtPorcentaje.getValue();
                editar.setPorcentaje(l.floatValue());
            }else{
                editar.setPorcentaje((Float) jtPorcentaje.getValue());
            }
            editar.setRespuestas(respuestas);
            editar.setRespuesta(jList1.getSelectedValue().toString());
            c.actualizarPregunta(editar);
        }
        JOptionPane.showMessageDialog(this, "Pregunta almacenada con exito");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String resp = JOptionPane.showInputDialog(this, "Ingrese la nueva respuesta");
        if (resp != null && !resp.isEmpty()) {
            Respuesta r = new Respuesta();
            r.setValor(resp);
            respuestas.add(r);
            recargarLista();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jList1.getSelectedValue() != null) {
            respuestas.remove((Respuesta) jList1.getSelectedValue());
            recargarLista();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

